package ru.vmsoftware.javafx.dialogs;

/**
 * Enum for standard dialog responses
 * @author Vyacheslav Mayorov
 * @since 2013-01-10
 */
public enum DialogResult {

    /**
     * Dialog close button clicked
     */
    CLOSED,

    /**
     * Yes button clicked
     */
    YES,

    /**
     * No button clicked
     */
    NO,

    /**
     * Cancel button clicked
     */
    CANCEL,

    /**
     * Ok button clicked
     */
    OK,
}
